import re

string2 = """
950825-1234567
850612-2987654
930525-2876552
940808-2394945
841019-1102913
870718-1203913
910803-2113709
871001-1987121
951203-1655786
931213-1861930
900312-2908133
871032-1543922
911213-2877651
"""

male = re.findall(r'[-][1]', string2)
female = re.findall(r'[-][2]', string2)
ninety = re.findall(r'[9].{5}[-]', string2)
eighty = re.findall(r'[8].{5}[-]', string2)
print('80년도생', len(eighty))
print('90년도생', len(ninety))
print('남자', len(male))
print('여자', len(female))
